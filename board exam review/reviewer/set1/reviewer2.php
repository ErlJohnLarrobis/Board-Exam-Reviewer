            <h2>PNLE II for Community Health Nursing and Care of the Mother and Child</h2>
            <p>
                1. May arrives at the health care clinic and tells the nurse that her last
                menstrual period was 9 weeks ago. She also tells the nurse that a home pregnancy
                test was positive but she began to have mild cramps and is now having moderate
                vaginal bleeding. During the physical examination of the client, the nurse notes
                that May has a dilated cervix. The nurse determines that May is experiencing
                which type of abortion?<br>
                <strong>Inevitable. An inevitable abortion is termination of pregnancy that
                    cannot be prevented. Moderate to severe bleeding with mild cramping and cervical
                    dilation would be noted in this type of abortion.</strong>
            </p>
            <p>
                2. Nurse Reese is reviewing the record of a pregnant client for her first
                prenatal visit. Which of the following data, if noted on the client’s record,
                would alert the nurse that the client is at risk for a spontaneous abortion?<br>
                <strong>History of syphilis. Maternal infections such as syphilis,
                    toxoplasmosis, and rubella are causes of spontaneous abortion.</strong>
            </p>
            <p>
                3. Nurse Hazel is preparing to care for a client who is newly admitted to the
                hospital with a possible diagnosis of ectopic pregnancy. Nurse Hazel develops a
                plan of care for the client and determines that which of the following nursing
                actions is the priority?<br>
                <strong>Monitoring apical pulse. Nursing care for the client with a possible
                    ectopic pregnancy is focused on preventing or identifying hypovolemic shock and
                    controlling pain. An elevated pulse rate is an indicator of shock.</strong>
            </p>
            <p>
                4. Nurse Oliver is teaching a diabetic pregnant client about nutrition and
                insulin needs during pregnancy. The nurse determines that the client understands
                dietary and insulin needs if the client states that the second half of pregnancy
                require:<br>
                <strong>Increased caloric intake. Glucose crosses the placenta, but insulin does
                    not. High fetal demands for glucose, combined with the insulin resistance caused
                    by hormonal changes in the last half of pregnancy can result in elevation of
                    maternal blood glucose levels. This increases the mother’s demand for insulin
                    and is referred to as the diabetogenic effect of pregnancy.</strong>
            </p>
            <p>
                5. Nurse Michelle is assessing a 24 year old client with a diagnosis of
                hydatidiform mole. She is aware that one of the following is unassociated with
                this condition?<br>
                <strong>Excessive fetal activity. The most common signs and symptoms of
                    hydatidiform mole includes elevated levels of human chorionic gonadotropin,
                    vaginal bleeding, larger than normal uterus for gestational age, failure to
                    detect fetal heart activity even with sensitive instruments, excessive nausea
                    and vomiting, and early development of pregnancy-induced hypertension. Fetal
                    activity would not be noted.
                </strong>
            </p>
            <p>
                6. A pregnant client is receiving magnesium sulfate for severe pregnancy induced
                hypertension (PIH). The clinical findings that would warrant use of the antidote
                , calcium gluconate is:<br>
                <strong>Absent patellar reflexes. Absence of patellar reflexes is an indicator
                    of hypermagnesemia, which requires administration of calcium gluconate.</strong>
            </p>
            <p>
                7. During vaginal examination of Janah who is in labor, the presenting part is
                at station plus two. Nurse, correctly interprets it as:<br>
                <strong>Presenting part in 2 cm below the plane of the ischial spines. Fetus at
                    station plus two indicates that the presenting part is 2 cm below the plane of
                    the ischial spines.</strong>
            </p>
            <p>
                8. A pregnant client is receiving oxytocin (Pitocin) for induction of labor. A
                condition that warrant the nurse in-charge to discontinue I.V. infusion of
                Pitocin is:<br>
                <strong>Contractions every 1 ½ minutes lasting 70-80 seconds. Contractions every
                    1 ½ minutes lasting 70-80 seconds, is indicative of hyperstimulation of the
                    uterus, which could result in injury to the mother and the fetus if Pitocin is
                    not discontinued.</strong>
            </p>
            <p>
                9. Calcium gluconate is being administered to a client with pregnancy induced
                hypertension (PIH). A nursing action that must be initiated as the plan of care
                throughout injection of the drug is:<br>
                <strong>EKG tracings. A potential side effect of calcium gluconate
                    administration is cardiac arrest. Continuous monitoring of cardiac activity
                    (EKG) throught administration of calcium gluconate is an essential part of care.</strong>
            </p>
            <p>
                10. A trial for vaginal delivery after an earlier caesareans, would likely to be
                given to a gravida, who had:<br>
                <strong>First low transverse caesarean was for breech position. Fetus in this
                    pregnancy is in a vertex presentation. This type of client has no obstetrical
                    indication for a caesarean section as she did with her first caesarean delivery.</strong>
            </p>
            <p>
                11.Nurse Ryan is aware that the best initial approach when trying to take a
                crying toddler’s temperature is:
                <br>
                <strong>
                    Talk to the mother first and then to the toddler. When dealing with a crying
                    toddler, the best approach is to talk to the mother and ignore the toddler
                    first. This approach helps the toddler get used to the nurse before she attempts
                    any procedures. It also gives the toddler an opportunity to see that the mother
                    trusts the nurse.
                </strong>
            </p>
            <p>
                12.Baby Tina a 3 month old infant just had a cleft lip and palate repair. What
                should the nurse do to prevent trauma to operative site?
                <br>
                <strong>
                    Place the infant’s arms in soft elbow restraints. Soft restraints from the upper
                    arm to the wrist prevent the infant from touching her lip but allow him to hold
                    a favorite item such as a blanket. Because they could damage the operative site,
                    such as objects as pacifiers, suction catheters, and small spoons shouldn’t be
                    placed in a baby’s mouth after cleft repair. A baby in a prone position may rub
                    her face on the sheets and traumatize the operative site. The suture line should
                    be cleaned gently to prevent infection, which could interfere with healing and
                    damage the cosmetic appearance of the repair.
                </strong>
            </p>
            <p>
                13. Which action should nurse Marian include in the care plan for a 2 month old
                with heart failure?
                <br>
                <strong>
                    Allow the infant to rest before feeding. Because feeding requires so much
                    energy, an infant with heart failure should rest before feeding
                </strong>
            </p>
            <p>
                14.Nurse Hazel is teaching a mother who plans to discontinue breast feeding
                after 5 months. The nurse should advise her to include which foods in her
                infant’s diet?
                <br>
                <strong>
                    Iron-rich formula only. The infants at age 5 months should receive iron-rich
                    formula and that they shouldn’t receive solid food, even baby food until age 6
                    months.
                </strong>
            </p>
            <p>
                15.Mommy Linda is playing with her infant, who is sitting securely alone on the
                floor of the clinic. The mother hides a toy behind her back and the infant looks
                for it. The nurse is aware that estimated age of the infant would be:
                <br>
                <strong>
                    10 months. A 10 month old infant can sit alone and understands object
                    permanence, so he would look for the hidden toy. At age 4 to 6 months, infants
                    can’t sit securely alone. At age 8 months, infants can sit securely alone but
                    cannot understand the permanence of objects.
                </strong>
            </p>
